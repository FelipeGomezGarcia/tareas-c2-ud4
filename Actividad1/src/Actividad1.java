
public class Actividad1 {
	public static void main (String args[]) {
		
		int num1 = 10 , num2 = 3;
		double resultado;
		
		resultado = num1/num2;
		
		System.out.println(num1 + " + "+ num2 + " = " + (num1+num2));
		System.out.println(num1 + " - "+ num2 + " = " + (num1-num2));
		System.out.println(num2 + " - "+ num1 + " = " + (num2-num1));
		System.out.println(num1 + " * "+ num2 + " = " + (num1*num2));
		System.out.println(num1 + " : "+ num2 + " = " + resultado);
		System.out.println(num1 + " % "+ num2 + " = " + (num1%num2));
	}
}
